//Addition Function

var add = function(sum1, sum2) {
  return sum1 + sum2;
};

var number1 = parseInt(prompt("Enter a number to add:"));
var number2 = parseInt(prompt("Enter another number to add:"));

alert(add(number1, number2));

//Subtraction Function

var subtract = function(difference1, difference2) {
  return difference1 - difference2;
};

var number3 = parseInt(prompt("Enter a number to subtract:"));
var number4 = parseInt(prompt("Enter another number to subtract:"));

alert(subtract(number3, number4));

// //Multiplication Function

